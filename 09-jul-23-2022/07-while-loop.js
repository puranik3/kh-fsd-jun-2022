let repeat = 10;

while( repeat > 0 ) {
    console.log( 'repeat = ', repeat );
    console.log( 'hello' );
    console.log( 'world' );

    // decrement operator (pre-decrement)
    --repeat; // repeat = repeat - 1;
}

console.log( 'end of repetitions' );

